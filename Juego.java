
/**
* Un objeto de esta clase guarda información relativa a un juego
* 
* @author - Richard Eguaras
*/
public class Juego {
    private static final String SEPARADOR = ":";
    private String titulo;
    private Genero genero;
    private int year;
    private int[] valoraciones;

    /**
    *  Inicializa los atributos a partir de la información recibida
    *  Esta información se encuentra en linea
    */
    public Juego(String linea) 
    {
            
            String[] datos = linea.split(":");
            datos[0].toUpperCase();
            titulo = datos[0];
           switch(datos[1])
            {
                case "aventura" : genero = genero.valueOf("AVENTURA");
                break;
                case "deporte": genero = genero.valueOf("DEPORTE");
                break;
                case "estrategia": genero = genero.valueOf("ESTRATEGIA");
                break;
                case "rol": genero = genero.valueOf("ROL");
                break;
            }
            int año = Integer.parseInt(datos[2].trim());
            year = año;
            valoraciones = new int[10];
            int aux = 3;
            for(int i = 0; i < 10; i++)
            {
                valoraciones[i] = Integer.parseInt(datos[aux].trim());
                aux++;
            }
            
           
    }

    /**
     * accesor título
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * accesor género
     */
    public Genero getGenero() {
        return genero;
    }

    /**
     * accesor year
     */
    public int getYear() {
        return year;
    }

    /**
     * accesor valoraciones
     */
    public int[] getValoraciones() {
        return valoraciones;
    }

    /**
     * total votos emitidos
     */
    public int getVotos() {
        int aux = 0;
        for(int i = 0; i < 10; i++)
        {
          aux += valoraciones[i];
          
        }
        return aux;
    }

    /**
     *  obtener valoración media
     */
    public double getValoracionMedia() {
        double media = 0;
        int[] aux;
        aux = new int[10];
        for(int i = 0; i < 10; i++)
        {
         aux[i] = valoraciones[i] * i;
         media += aux[i];
        }
        media = media / aux.length;
        return media;
        
    }

    /**
     *  Un usuario puntúa el juego con un valor entre 1 y 10 
     */
    public void puntuar(int puntuacion) 
    {
        for(int i = 0; i < 10; i++)
        {
          if (puntuacion == i + 1)
          {
            valoraciones[i]++;
          }
        }
    }

    /**
     * Representación textual del juego 
     * (Ver enunciado)
     */
    public String toString() {
        return titulo + "\nGénero: " + this.genero +
                            "| Lanzamiento: " + this.year +
                            "\nValoración (" + getVotos() + " votos): "
                            + String.format("%.2f", this.getValoracionMedia());

    }

    public static void main(String[] args) {
        Juego juego1 = new Juego(
                            "Steep: deportes: 2016  : 0:0:0:0: 0: 0:0:0:12:  10");
        System.out.println(juego1.toString());

        Juego juego2 = new Juego(
                            "For the King: estrategia: 2018  : 0:0:0:7: 12: 0:33:13:2: 0");
        System.out.println(juego2.toString());

    }
}
